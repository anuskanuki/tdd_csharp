

namespace Application.Domain
{
    public class Calculator
    {
        public int IntSum(int number1, int number2)
        {
            return number1 + number2;
        }

        public double DoubleSum(double number1, double number2)
        {
            return number1 + number2;
        }

        public int SubtractInt(int number1, int number2)
        {
            return number1 - number2;
        }

        public double SubtractDouble(double number1, double number2)
        {
            return number1 - number2;
        }

        public int MultiplyInt(int number1, int number2)
        {
            return number1 * number2;
        }

        public double MultiplyDouble(double number1, double number2)
        {
            return number1 * number2;
        }

        public int DivideInt(int number1, int number2)
        {
            return number1 / number2;
        }

        public double DivideDouble(double number1, double number2)
        {
            return number1 / number2;
        }

    }
}
